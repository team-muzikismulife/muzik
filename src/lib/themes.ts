/** dateKey('YYYY-MM-DD') → 테마. 간단한 문자열 해시로 결정론적 선택 */
export function themeFor(dateKey: string): string {
  let hash = 0;
  for (let i = 0; i < dateKey.length; i++) {
    hash = (hash * 31 + dateKey.charCodeAt(i)) >>> 0;
  }
  return THEMES[hash % THEMES.length];
}
